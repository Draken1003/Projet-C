#include "math.h"

double addition(double a, double b)
{
	double res = a + b;
	return res;
}

double soustraction(double a, double b)
{
	double res = a - b;
	return res;
}

double multiplication(double a, double b)
{
	double res = a * b;
	return res;
}

double division(double a, double b)
{
	double res = a / b;
	return res;
}
